class UnaryDemo{

	public static void main(String[]args){

		int num = 10;
		 
		System.out.println(+num);
		System.out.println(-num);
		System.out.println(++num);
		System.out.println(--num);

		//System.out.println(+num);    ---error

		boolean num1 = true;
		System.out.println(num1);
		System.out.println(!num1);
	}
}
