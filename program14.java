class ArithmeticDemo{

	public static void main(String[]args){

		int num1 = 10;
		int num2 = 3;
		int ans = 0;

		ans = num1 + num2;
		System.out.println("ans = "+ans);
		
		ans = num1 * num2;
		System.out.println("ans = "+ans);

		ans = num1 / num2;
		System.out.println("ans = "+ans);

		ans = num1 % num2;
		System.out.println("ans = "+ans);
	}
}
